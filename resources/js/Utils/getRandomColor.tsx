export const getRandomColor = (type="bg") => {
    const colors = type === "bg" ? [
        "bg-red-500",
        "bg-blue-500",
        "bg-green-500",
        "bg-yellow-500",
        "bg-purple-500",
        "bg-pink-500",
        "bg-teal-500",
        "bg-indigo-500",
        "bg-rose-300",
        "bg-pink-300",
        "bg-fuchsia-300",
        "bg-purple-300",
        "bg-violet-300",
        "bg-indigo-300",
        "bg-blue-300",
        "bg-sky-300",
        "bg-cyan-300",
        "bg-teal-300",
        "bg-emerald-300",
        "bg-green-300",
        "bg-lime-300",
        "bg-yellow-300",
        "bg-amber-300",
        "bg-orange-300",
        "bg-red-300",
        "bg-gray-300",
    ] : [
        "text-red-500",
        "text-blue-500",
        "text-green-500",
        "text-yellow-500",
        "text-purple-500",
        "text-pink-500",
        "text-teal-500",
        "text-indigo-500",
        "text-rose-300",
        "text-pink-300",
        "text-fuchsia-300",
        "text-purple-300",
        "text-violet-300",
        "text-indigo-300",
        "text-blue-300",
        "text-sky-300",
        "text-cyan-300",
        "text-teal-300",
        "text-emerald-300",
        "text-green-300",
        "text-lime-300",
        "text-yellow-300",
        "text-amber-300",
        "text-orange-300",
        "text-red-300",
        "text-gray-300",
    ];
    const randomIndex = Math.floor(Math.random() * colors.length);
    return colors[randomIndex];
};
